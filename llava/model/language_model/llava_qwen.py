    @torch.no_grad()
    def custom_generate(
        self,
        input_is_memory,
        inputs: Optional[torch.Tensor] = None,
        images: Optional[torch.Tensor] = None,
        image_sizes: Optional[torch.Tensor] = None,
        modalities: Optional[List[str]] = ["image"],
        get_logits: Optional[bool] = False,
        output_hidden_states=False,
        attentions=False,
        **kwargs,
    ) -> Union[GenerateOutput, torch.LongTensor]:
        position_ids = kwargs.pop("position_ids", None)
        attention_mask = kwargs.pop("attention_mask", None)
        if "inputs_embeds" in kwargs:
            raise NotImplementedError("`inputs_embeds` is not supported")

        self.input_is_memory = input_is_memory
        if images is not None:
            (inputs, position_ids, attention_mask, _, inputs_embeds, _, visual_tokens) = self.custom_prepare_inputs_labels_for_multimodal(inputs, position_ids, attention_mask, None, None, images, modalities, image_sizes=image_sizes)
        else:
            inputs_embeds = self.get_model().embed_tokens(inputs)
            visual_tokens = None

        return visual_tokens, super().generate(position_ids=position_ids, attention_mask=attention_mask, use_cache=True,  inputs_embeds=inputs_embeds, output_hidden_states=output_hidden_states, return_dict_in_generate=True, output_attentions=attentions, **kwargs)     
